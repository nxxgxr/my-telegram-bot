async def create_crypto_invoice(user_id: int, username: str) -> dict:
    """–°–æ–∑–¥–∞–Ω–∏–µ –∏–Ω–≤–æ–π—Å–∞ –≤ Crypto Bot."""
    try:
        # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –ª–∏—Ü–µ–Ω–∑–∏–æ–Ω–Ω—ã–π –∫–ª—é—á –∑–∞—Ä–∞–Ω–µ–µ
        license_key = generate_license()
        
        headers = {
            "Crypto-Pay-API-Token": CRYPTO_BOT_TOKEN,
            "Content-Type": "application/json"
        }
        
        payload = {
            "amount": PAYMENT_AMOUNT,
            "currency": PAYMENT_CURRENCY,
            "expires_in": PAYMENT_EXPIRES_IN,
            "description": f"–õ–∏—Ü–µ–Ω–∑–∏—è Valture –¥–ª—è @{username}",
            "hidden_message": f"–ü–æ—Å–ª–µ –æ–ø–ª–∞—Ç—ã –≤—ã –ø–æ–ª—É—á–∏—Ç–µ –ª–∏—Ü–µ–Ω–∑–∏–æ–Ω–Ω—ã–π –∫–ª—é—á.",
            "payload": str(user_id)  # –î–ª—è –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–∏ –ø–ª–∞—Ç–µ–∂–∞
        }
        
        response = requests.post(
            f"{CRYPTO_BOT_API_URL}/createInvoice",
            headers=headers,
            json=payload
        )
        
        response.raise_for_status()
        data = response.json()
        
        if data.get("ok"):
            invoice = data["result"]
            
            # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–ª–∞—Ç–µ–∂–µ
            active_payments[user_id] = {
                "invoice_id": invoice["invoice_id"],
                "license_key": license_key,
                "paid": False,
                "username": username
            }
            
            return invoice
        
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∏–Ω–≤–æ–π—Å–∞: {data}")
        return None
        
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∏–Ω–≤–æ–π—Å–∞ Crypto Bot: {e}")
        return None

async def check_payment_status(invoice_id: str) -> bool:
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç–∞—Ç—É—Å–∞ –ø–ª–∞—Ç–µ–∂–∞ –≤ Crypto Bot."""
    try:
        headers = {
            "Crypto-Pay-API-Token": CRYPTO_BOT_TOKEN
        }
        
        response = requests.get(
            f"{CRYPTO_BOT_API_URL}/getInvoices?invoice_ids={invoice_id}",
            headers=headers
        )
        
        response.raise_for_status()
        data = response.json()
        
        if data.get("ok"):
            invoice = data["result"]["items"][0]
            return invoice.get("status") == "paid"
        
        return False
        
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ —Å—Ç–∞—Ç—É—Å–∞ –ø–ª–∞—Ç–µ–∂–∞: {e}")
        return False

